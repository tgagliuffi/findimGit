package com.bbva.findim.dom;

public class TarifaBean {
	
	private String codigoTarifa;
	private String cuota;
	private String tasa;
	private String descripcion;
	private String moneda;
	private String tasaSeguro;
	private String cdSubProducto;
		
	public String getCodigoTarifa() {
		return codigoTarifa;
	}
	public void setCodigoTarifa(String codigoTarifa) {
		this.codigoTarifa = codigoTarifa;
	}
	public String getCuota() {
		return cuota;
	}
	public void setCuota(String cuota) {
		this.cuota = cuota;
	}
	public String getTasa() {
		return tasa;
	}
	public void setTasa(String tasa) {
		this.tasa = tasa;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getMoneda() {
		return moneda;
	}
	public void setMoneda(String moneda) {
		this.moneda = moneda;
	}
	public String getCdSubProducto() {
		return cdSubProducto;
	}
	public void setCdSubProducto(String cdSubProducto) {
		this.cdSubProducto = cdSubProducto;
	}
	public String getTasaSeguro() {
		return tasaSeguro;
	}
	public void setTasaSeguro(String tasaSeguro) {
		this.tasaSeguro = tasaSeguro;
	}
	

	
}
