package com.bbva.findim.dom;

public class FileUnicoRest {

	private String pathArchivo;
	private String val;
	private String rutaContratosFirmados;
	private String rutaDestinoFU;
	
	
	
	public String getRutaContratosFirmados() {
		return rutaContratosFirmados;
	}
	public void setRutaContratosFirmados(String rutaContratosFirmados) {
		this.rutaContratosFirmados = rutaContratosFirmados;
	}
	public String getRutaDestinoFU() {
		return rutaDestinoFU;
	}
	public void setRutaDestinoFU(String rutaDestinoFU) {
		this.rutaDestinoFU = rutaDestinoFU;
	}
	public String getPathArchivo() {
		return pathArchivo;
	}
	public void setPathArchivo(String pathArchivo) {
		this.pathArchivo = pathArchivo;
	}
	public String getVal() {
		return val;
	}
	public void setVal(String val) {
		this.val = val;
	}
	
	
}
