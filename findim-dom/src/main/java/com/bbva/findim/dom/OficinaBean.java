package com.bbva.findim.dom;

public class OficinaBean {
	
	private Long idOficina;
	private String cdOficina;
	private String nbOficina;
	private String cdUbigeoHost;
	
	public Long getIdOficina() {
		return idOficina;
	}
	public void setIdOficina(Long idOficina) {
		this.idOficina = idOficina;
	}
	public String getCdOficina() {
		return cdOficina;
	}
	public void setCdOficina(String cdOficina) {
		this.cdOficina = cdOficina;
	}
	public String getNbOficina() {
		return nbOficina;
	}
	public void setNbOficina(String nbOficina) {
		this.nbOficina = nbOficina;
	}
	public String getCdUbigeoHost() {
		return cdUbigeoHost;
	}
	public void setCdUbigeoHost(String cdUbigeoHost) {
		this.cdUbigeoHost = cdUbigeoHost;
	}
	
	

}
